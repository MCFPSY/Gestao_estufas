-- Criar tabela de transportadores
CREATE TABLE IF NOT EXISTS transportadores (
    id SERIAL PRIMARY KEY,
    nome TEXT NOT NULL UNIQUE,
    ativo BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Permitir leitura pública (anon pode ler)
ALTER TABLE transportadores ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read" ON transportadores FOR SELECT TO anon USING (true);
CREATE POLICY "Allow public insert" ON transportadores FOR INSERT TO anon WITH CHECK (true);
CREATE POLICY "Allow public update" ON transportadores FOR UPDATE TO anon USING (true);

